;FIND SQUARE OF NUMBER WITHOUT MULT.
.MODEL SMALL

PRINTS MACRO P
LEA DX,P
MOV AH,09H
INT 21H
ENDM

.DATA
STRING1 DB "ENTER A NUMBER:$"
STRING2 DB 10,13,"SQUARE IS $"


.CODE
MOV AX,@DATA
MOV DS,AX

PRINTS STRING1
MOV AH,01H
INT 21H
SUB AL,'0'
MOV BL,AL
MOV AL,0
MOV CL,BL

BACK:
ADD AL,BL
LOOP BACK

PRINTS STRING2
MOV AH,02H
MOV DL,AL
ADD DL,'0'
INT 21H

EXIT:
MOV AH,4CH
INT 21H
END