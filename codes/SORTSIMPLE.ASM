

.MODEL SMALL

PRINTN MACRO P 
    LEA DX,P
    MOV AH,09H
    INT 21H
ENDM

PRINTNUM MACRO N ;PRINT  NUMBER
    MOV DL,N
    MOV AH,02H
    ADD DL,30H
    INT 21H
ENDM

.DATA 
ARRAY DB 2,4,1,6,8,3
STRING DB 10,13,"ARRAY IS:$"

.CODE 
MOV AX, @DATA
MOV DS, AX
MOV CL,5


;BUBBLE SORT

FIRST: ;FIRST LOOP I=0,I<N,I++
MOV BH,1
LEA DI,ARRAY


SECOND: ; SECOND LOOP J=0,J<N-1,J++
MOV AL,[DI]
MOV BL,[DI+1]
CMP AL,BL
JG SWAP


NEXT:
INC DI
INC BH
CMP BH,CL
JL SECOND
LOOP FIRST
JMP EXIT

SWAP:
MOV [DI],BL
MOV [DI+1],AL
JMP NEXT

EXIT:
PRINTN STRING
LEA SI,ARRAY
MOV CL,5

DISPLAY:
PRINTNUM [SI]
INC SI
LOOP DISPLAY

MOV AH,4CH
INT 21H
END
