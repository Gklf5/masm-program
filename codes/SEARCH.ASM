;READ A STRING AND KEY, CHECK IF KEY IS IN STRING.

.MODEL SMALL

PRINTS MACRO P
LEA DX,P
MOV AH,09H
INT 21H
ENDM

.DATA
BUFFER DB 20,?
STRING1 DB "ENTER A STRING:$"
STRING2 DB 10,13,"ENTER A KEY:$"
STRING3 DB 10,13,"KEY FOUND$"
STRING4 DB 10,13,"KEY NOT FOUND$"

.CODE
MOV AX,@DATA
MOV DS,AX

PRINTS STRING1
LEA DX,BUFFER
MOV AH,0AH
INT 21H

PRINTS STRING2
MOV AH,01H
INT 21H
MOV BL,AL


LEA SI,BUFFER+2

SCAN:
MOV AL,[SI]
CMP AL,13
JE NOT_FOUND
CMP AL,BL
JE FOUND
INC SI
JMP SCAN

FOUND:
PRINTS STRING3
JMP EXIT

NOT_FOUND:
PRINTS STRING4

EXIT:
MOV AH,4CH
INT 21H
END