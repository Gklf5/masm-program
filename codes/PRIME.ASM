;READ A NUMBER, CHECK PRIME OR NOT.

.MODEL SMALL

PRINTS MACRO P
LEA DX,P
MOV AH,09H
INT 21H
ENDM

.DATA
NUMBER DB 0
STRING1 DB "ENTER A NUMBER:$"
STRING2 DB 10,13,"IS PRIME$"
STRING3 DB 10,13,"IS NOT PRIME$"

.CODE
MOV AX,@DATA
MOV DS,AX

PRINTS STRING1
MOV AH,01H
INT 21H
SUB AL,'0'

CMP AL,1
JE NOT_PRIME

MOV NUMBER,AL
MOV AH,0
MOV BL,2

BACK:
CMP BL,NUMBER
JE PRIME
MOV AL,NUMBER
MOV AH,0
DIV BL
CMP AH,0
JE NOT_PRIME
INC BL
JMP BACK

PRIME:
PRINTS STRING2
JMP EXIT


NOT_PRIME:
PRINTS STRING3



EXIT:
MOV AH,4CH
INT 21H
END