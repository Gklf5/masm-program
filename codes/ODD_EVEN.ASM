;INPUT A NUMBER, PRINT ODD OR EVEN

.MODEL SMALL

PRINTS MACRO P
LEA DX,P
MOV AH,09H
INT 21H
ENDM

.DATA
STRING1 DB "ENTER A NUMBER:$"
STRING2 DB 10,13,"NO. IS ODD$"
STRING3 DB 10,13,"NO. IS EVEN$"

.CODE
MOV AX,@DATA
MOV DS,AX

PRINTS STRING1
MOV AH,01H
INT 21H
SUB AL,'0'
MOV BL,2

DIV BL
CMP AH,0
JNE ODD
PRINTS STRING3
JMP EXIT

ODD:
PRINTS STRING2

EXIT:
MOV AH,4CH
INT 21H
END