;READING AN ARRAY, AND SORTING IN ASC ORDER

.MODEL SMALL

PRINTN MACRO P
    LEA DX,P
    MOV AH,09H
    INT 21H
ENDM

PRINTNUM MACRO N ;PRINT NUMBER
    MOV DL,N
    MOV AH,02H
    ADD DL,30H
    INT 21H
ENDM

.DATA
ARRAY DB 20,?
STRING1 DB "ENTER ARRAY:$"
STRING DB 10,13,"SORTED ARRAY IS:$"
SPACE DB "  $" ;FOR SPACING ARRAY ELEMENTS

.CODE
MOV AX, @DATA
MOV DS, AX
MOV CL,5

PRINTN STRING1

LEA DI,ARRAY
MOV AH,01H
READ:
INT 21H
CMP AL,13
JE OUT1
SUB AL,30H
MOV [DI],AL
INC DI
INC CL
JMP READ

OUT1:
MOV CH,CL

FIRST: ;FIRST LOOP I=0,I<N,I++
    MOV BH,1
    LEA DI,ARRAY

SECOND: ; SECOND LOOP J=0,J<N-1,J++
    MOV AL,[DI]
    MOV BL,[DI+1]
    CMP AL,BL
    JG SWAP

NEXT:
    INC DI
    INC BH
    CMP BH,CH
    JL SECOND
    DEC CH
    CMP CH,0
    JG FIRST
    JMP EXIT

SWAP:
    MOV [DI],BL
    MOV [DI+1],AL
    JMP NEXT

EXIT:
    PRINTN STRING
    LEA SI,ARRAY
    MOV CL,5

DISPLAY:
    PRINTN SPACE
    PRINTNUM [SI]
    INC SI
    LOOP DISPLAY

MOV AH,4CH
INT 21H
END
